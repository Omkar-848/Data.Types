package DataTypes;

public class Primitive {

	        public static void main(String[] args) {
	        // 1. byte - 8-bit signed integer
	        byte byteValue = 100;
	        System.out.println("Byte value: " + byteValue);

	        // 2. short - 16-bit signed integer
	        short shortValue = 2000;
	        System.out.println("Short value: " + shortValue);

	        // 3. int - 32-bit signed integer
	        int intValue = 50000;
	        System.out.println("Int value: " + intValue);

	        // 4. long - 64-bit signed integer
	        long longValue = 100000L;  // 'L' suffix denotes a long value
	        System.out.println("Long value: " + longValue);

	        // 5. float - Single-precision 32-bit floating-point
	        float floatValue = 5.75f;  // 'f' suffix denotes a float value
	        System.out.println("Float value: " + floatValue);

	        // 6. double - Double-precision 64-bit floating-point
	        double doubleValue = 19.99;
	        System.out.println("Double value: " + doubleValue);

	        // 7. char - 16-bit Unicode character
	        char charValue = 'A';
	        System.out.println("Char value: " + charValue);

	        // 8. boolean - Represents true or false
	        boolean booleanValue = true;
	        System.out.println("Boolean value: " + booleanValue);
	    }
	}

	

